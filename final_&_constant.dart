void main() {
  // final keyword
  final city;
  city = "Gombe";
  final int num1 = 10; // writing the data type "int" is optional

  //const keyword
  const PI = 3.142;
  const double gravity = 9.8; // writing the data type "double" is optional

  print("I'm from  $city state ");
  print("value of num1 is $num1");
  print("The constant value of PI is $PI");
  print("The constant value of acceleration due to gravity is $gravity");
}
